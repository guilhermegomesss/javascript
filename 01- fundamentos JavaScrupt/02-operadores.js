// Igualdade ==
let num1 = 10
let num2 = "10"

console.log(num1 == num2)

// Igualdade ===
console.log(num1 === num2)

// Diferente !=
const diferente1 = 10
const diferente2 = "10"
console.log(diferente1 != diferente2)

// Diferente !==
console.log(diferente1 !== diferente2)

// Maior que <

console.log(20 > 10)

// Menor que <
console.log(20 < 10)

// Maior igual >=
console.log(10 >= 10)

// Menor igual <=
